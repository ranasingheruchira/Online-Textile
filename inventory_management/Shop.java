package com.jayasadha.inventory_management;



public class Shop {

	
	private int  itID;
	private String itName;
	private String itPrice;
	private String itImage;
	

	
    
	public Shop(int itID, String itName, String itPrice,String itImage) {
		
		this.itID = itID;
		this.itName = itPrice;
		this.itPrice = itPrice;
		this.itImage = itImage;
	}
	
	
	public int getitID() {
		return itID;
	}


	public String getitName() {
		return itName;
	}


	public String getitPrice() {
		return itPrice;
	}
	
	public String getitImage() {
		return itImage;
	}










}
